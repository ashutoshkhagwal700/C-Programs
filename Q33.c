// Program to print size of an int, a float, a char and a double type variable.

#include<stdio.h>
int main(){
    int i;
    float f;
    char c;
    double d;
    printf("size of i= %d\n",sizeof(i));
    printf("size of f= %d\n",sizeof(f));
    printf("size of c= %d\n",sizeof(c));
    printf("size of d= %d\n",sizeof(d));
    return 0;

}