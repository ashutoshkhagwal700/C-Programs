// Program to input three character from user and display character with their corresponding ASCII codes.

#include<stdio.h>
int main(){
    char a,b,c;
    printf("enter character");
    scanf("%c%c%c",&a,&b,&c);
    printf("%c - %d\n",a,a);
    printf("%c - %d\n",b,b);
    printf("%c - %d\n",c,c);
    return 0;

}